-- Formulation avec jointure externe
SELECT PROPRIO.IDP, NOM, PRENOM
FROM PROPRIO
LEFT OUTER JOIN BIEN
ON PROPRIO.IDP = BIEN.IDP
AND BIEN.VILLE = 'MARSEILLE'
WHERE IDEBIEN IS NULL
ORDER BY PROPRIO.IDP ASC;

