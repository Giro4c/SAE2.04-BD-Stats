-- Création des tables

CREATE TABLE PROPRIO (
    IdP NUMBER(4),
    Nom VARCHAR2(15),
    Prenom VARCHAR2(15),
    CONSTRAINT PK_PROPRIO PRIMARY KEY (IdP)
)
/
CREATE TABLE ESPACE (
    IdE NUMBER(4),
    Designation VARCHAR2(20),
    Superficie NUMBER(4),
    IdERattach NUMBER(4),
    CONSTRAINT PK_ESP PRIMARY KEY (IdE),
    CONSTRAINT FK_ESP FOREIGN KEY (IdERattach) REFERENCES ESPACE (IdE)
)
/
CREATE TABLE BIEN (
    IdEBien NUMBER(4),
    Categorie VARCHAR2(15),
    Type VARCHAR2(15),
    IdP NUMBER(4),
    Ville VARCHAR2(15),
    Prix NUMBER(7),
    CONSTRAINT PK_BIEN PRIMARY KEY (IdEBien),
    CONSTRAINT FK_BIEN FOREIGN KEY (IdP) REFERENCES PROPRIO (IdP),
    CONSTRAINT CK1_BIEN CHECK (Categorie IN ('APPARTEMENT', 'VILLA', 'MAISON DE VILLE')),
    CONSTRAINT CK2_BIEN CHECK (Type IN ('STUDIO','T2','T3','T4','T5','T6','T7','T8','T9','T10'))
)
