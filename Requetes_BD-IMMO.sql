-- Q5 :

SELECT IDEBIEN, PRIX, (PRIX - MIN_PRIX_BIEN) ECART_AVEC_MIN_BIEN
FROM BIEN, (
    SELECT MIN(PRIX) MIN_PRIX_BIEN
    FROM BIEN)
WHERE PRIX >= MIN_PRIX_BIEN
ORDER BY ECART_AVEC_MIN_BIEN ASC;

-- Q6 :

SELECT IDEBIEN, BIEN.CATEGORIE, PRIX, (PRIX - MIN_PRIX_CAT) ECART_AVEC_MIN_BIEN
FROM BIEN, (
    SELECT CATEGORIE, MIN(PRIX) MIN_PRIX_CAT
    FROM BIEN
    GROUP BY CATEGORIE) MIN_CAT
WHERE BIEN.CATEGORIE = MIN_CAT.CATEGORIE
AND PRIX >= MIN_PRIX_CAT
ORDER BY CATEGORIE, ECART_AVEC_MIN_BIEN ASC;

