SELECT IDP, COUNT(IDEBIEN)
FROM BIEN
WHERE IDP IN (
    SELECT IDP
    FROM BIEN
    WHERE TYPE='STUDIO'
    GROUP BY IDP
    HAVING COUNT(*) >= 2)
GROUP BY IDP
ORDER BY IDP ASC;

-- Q8 :

SELECT IDP, NOM, PRENOM
FROM PROPRIO P
WHERE IDP IN (
    SELECT IDP
    FROM BIEN
    WHERE CATEGORIE IS NOT NULL
    GROUP BY IDP
    HAVING COUNT(DISTINCT CATEGORIE) in (
        SELECT COUNT(DISTINCT CATEGORIE)
        FROM BIEN))
ORDER BY IDP ASC;

