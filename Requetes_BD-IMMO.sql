FROM BIEN
WHERE IDEBIEN IN (
    SELECT IDE
    FROM ESPACE
    WHERE IDERATTACH IS NULL
    CONNECT BY IDE = PRIOR IDERATTACH
    START WITH IDE IN (
        SELECT IDE
        FROM ESPACE, MAX_SUPERFICIE_TERRASSE
        WHERE DESIGNATION = 'TERRASSE'
        AND SUPERFICIE = MAX_SUP));

