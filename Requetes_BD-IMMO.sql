-- Q11 :

CREATE VIEW MIN_PRIX_CATEGORIE AS
SELECT CATEGORIE, MIN(PRIX) MIN_PRIX
FROM BIEN
GROUP BY CATEGORIE;

SELECT IDEBIEN, BIEN.CATEGORIE, PRIX, (PRIX - MIN_PRIX) ECART_AVEC_MIN_BIEN
FROM BIEN, MIN_PRIX_CATEGORIE
WHERE BIEN.CATEGORIE = MIN_PRIX_CATEGORIE.CATEGORIE
AND PRIX >= MIN_PRIX
ORDER BY CATEGORIE, ECART_AVEC_MIN_BIEN ASC;

